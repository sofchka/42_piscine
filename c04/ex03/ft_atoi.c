int	ft_atoi(char *str)
{
	int	i;
	int	j;
	int	num;

	i = 0;
	num = 0;
	j = 1;
	while (str[i] && ((str[i] <= 13 && str[i] >= 9) || str[i] == 32))
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			j *= -1;
		i++;
	}
	while (str[i] && str[i] <= '9' && str[i] >= '0')
	{
		num = (num * 10) + (str[i] - '0');
		i++;
	}
	return (num * j);
}
